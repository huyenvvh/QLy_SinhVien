    public String Checkgt(int gt) {
        if (gt == 1) {
            return "Nam";
        } else {
            return "Nữ";
        }
    }
    
    public void filltoTable(ArrayList<SinhVien> list) {
        DefaultTableModel model = (DefaultTableModel) table.getModel();
        model.setRowCount(0);
        for (SinhVien SV : list) {
            model.addRow(new Object[]{
                SV.getMaSV(), SV.getTenSV(), SV.getTenLop(), Checkgt(SV.getGioiTinh())
            
            });
        }
    }
    
    public void showData() {
        int row = table.getSelectedRow();
        txtmasv.setText(table.getValueAt(row, 0).toString());
        txttennv.setText(table.getValueAt(row, 1).toString());
        cbblop.setSelectedItem(table.getValueAt(row, 2));
        if (table.getValueAt(row, 3).equals("Nam")) {
            rdioNam.setSelected(true);
        } else {
            rdioNu.setSelected(true);
        }
    }

