    private void btnthemActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnthemActionPerformed
        // TODO add your handling code here:
        if (txtmasv.getText().equals("")) {
            JOptionPane.showMessageDialog(this, "Chua nhap ma!", "Error!", JOptionPane.ERROR_MESSAGE);
            txtmasv.requestFocus();
            return;
        }
        if (txttennv.getText().equals("")) {
            JOptionPane.showMessageDialog(this, "Chua nhap ten sinh vien!", "Error!", JOptionPane.ERROR_MESSAGE);
            txttennv.requestFocus();
            return;
        }
        if (buttonGroup1.getSelection() == null) {
            JOptionPane.showMessageDialog(this, "Chua chon gioi tinh!", "Error!", JOptionPane.ERROR_MESSAGE);
            return;
        }
        SinhVien sv = new SinhVien();
        sv.setMaSV(txtmasv.getText());
        sv.setTenSV(txttennv.getText());
        sv.setTenLop(cbblop.getSelectedItem().toString());
        if (rdioNam.isSelected()) {
            sv.setGioiTinh(1);
        } else {
            sv.setGioiTinh(0);
        }
        svService.them(sv);
        JOptionPane.showMessageDialog(this, "Da them!");
        filltoTable(svService.getalldata());
    }//GEN-LAST:event_btnthemActionPerformed

